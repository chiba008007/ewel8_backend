    public function getEAIB(Request $request)
    {
        $loginUser = auth()->user();
        $examId = $loginUser->id;
        $testpartsId = $request->testparts_id;

        // 最新の受検データを取得する
        $last = ExamEaib::where('testparts_id', $testpartsId)
            ->where('exam_id', $examId)
            ->latest('id')
            ->first();

        if (!$last) {
            return response(null, 200);
        }

        // 各ページで使用する最後の回答番号
        $pageEndKeys = [
            1 => 7,
            2 => 14,
            3 => 21,
            4 => 33,
            5 => 41,
            6 => 46,
            7 => 50,
            8 => 53,
            9 => 57,
            10 => 59,
            11 => 63,
            12 => 68,
            13 => 70,
            14 => 72,
            15 => 75,
            16 => 78,
            17 => 81,
            18 => 84,
            19 => 87,
            20 => 90,
            21 => 93,
            22 => 102,
            23 => 109,
            24 => 114,
            25 => 119,
            26 => 123,
            27 => 127,
            28 => 132,
            29 => 135,
            30 => 138,
            31 => 141,
            32 => 142,
        ];

        $currentPage = 1;
        $startKey = 1;

        foreach ($pageEndKeys as $page => $endKey) {
            // このページに未回答があるか確認する
            for ($i = $startKey; $i <= $endKey; $i++) {
                if ($last->{'q' . $i} === null) {
                    $currentPage = $page;
                    break 2;
                }
            }

            // このページが完了していれば次ページへ進める
            $currentPage = $page + 1;
            $startKey = $endKey + 1;
        }

        $last->current_page = min($currentPage, 32);

        return response($last, 200);
    }

    public function setEAIB(Request $request)
    {
        $loginUser = auth()->user();

        try {
            DB::transaction(function () use ($request, $loginUser) {
                // テスト時間を取得
                $timelimit = testparts::where('test_id', $loginUser->test_id)
                    ->where('code', self::CODE)
                    ->value('timelimit');

                // 既存の有効ステータスを無効化
                ExamEaib::where('exam_id', $loginUser->id)
                    ->where('status', 1)
                    ->update(['status' => 0]);

                // 新しい受検データを作成
                ExamEaib::create([
                    'testparts_id' => $request->testparts_id,
                    'exam_id'      => $loginUser->id,
                    'status'       => 1,
                    'starttime'    => now(),
                    'limittime'    => now()->addMinutes($timelimit),
                ]);
            });

            Log::info('EAIb開始成功', [
                'exam_id' => $loginUser->id,
                'test_id' => $loginUser->test_id,
                'ip' => $request->ip(),
            ]);

            return response('success', 200);

        } catch (\Throwable $e) {
            Log::error('EAIb開始失敗', [
                'exam_id' => $loginUser->id ?? null,
                'message' => $e->getMessage(),
                'line' => $e->getLine(),
                'ip' => $request->ip(),
            ]);

            return response('error', 500);
        }
    }

    public function editEAIB(Request $request)
    {

        /** @var \App\Models\Exam $user */
        $user = auth()->user();

        // 認証済みの受検者ID
        $exam_id = $user->id;
        $testparts_id = $request->testparts_id;

        Log::info('EAIB検査回答登録開始', [
            'exam_id' => $exam_id,
            'testparts_id' => $testparts_id,
            'page' => $request->page,
            'ip' => $request->ip(),
        ]);

        try {
            DB::transaction(function () use ($request, $exam_id, $testparts_id) {
                // 最新の受検データを取得
                $exam = ExamEaib::where('testparts_id', $testparts_id)
                    ->where('exam_id', $exam_id)
                    ->latest('id')
                    ->firstOrFail();

                // 2ページ目で開始時刻を更新
                if ((int)$request->page === 2) {
                    $exam->starttime = now();
                }

                // 回答を登録
                foreach ($request->selectPoint as $key => $value) {
                    $exam['q' . $key] = $value;
                }

                $exam->save();
                // 最終ページなら採点・完了処理
                if ((int)$request->page === 33) {

                    $this->getArrayPoint();
                    $score = [];

                    for ($i = 1; $i <= 143; $i++) {
                        $score[$i] = $exam['q' . $i];
                    }

                    $array_result = $this->getEAIBResult($score);



                    $exam->update([
                        'sougo'    => $array_result['sougo'],
                        'yomitori' => $array_result['yomitori'],
                        'rikai'    => $array_result['rikai'],
                        'sentaku'  => $array_result['sentaku'],
                        'kirikae'  => $array_result['kirikae'],
                        'jyoho'    => $array_result['jyoho'],
                        'endtime'  => now(),
                    ]);
                    Log::info('EAIB採点結果保存成功', [
                        'exam_id' => $exam_id,
                        'testparts_id' => $testparts_id,
                        'exambea_id' => $exam->id,
                    ]);

                    examfins::complete($exam_id, $testparts_id);
                    exam::setEndTime();

                    Log::info('受検者終了時刻更新成功', [
                        'exam_id' => $exam_id,
                    ]);

                    exam::sendRemainMail($request);
                    Log::info('残数メール処理成功', [
                        'exam_id' => $exam_id,
                        'testparts_id' => $testparts_id,
                    ]);

                }
            });

            return response('success', 200);

        } catch (\Throwable $e) {
            Log::error('EAIB検査回答登録失敗', [
                'exam_id' => $exam_id,
                'testparts_id' => $testparts_id,
                'page' => $request->page,
                'message' => $e->getMessage(),
                'line' => $e->getLine(),
                'ip' => $request->ip(),
            ]);

            return response('error', 500);
        }
    }

    public function getEAIBResult($rlt)
    {
        $array = $this->array_calc;
        $array_vf = $this->array_vf;

        // 集計変数をすべて初期化する
        $score = [];
        $infoP = 0;
        $infoM = 0;
        $read = 0;
        $change = 0;
        $understand = 0;
        $select = 0;

        $i = 1;

        foreach ($rlt as $k => $v) {

            $score[ $i ] = $array[ $i ][$v];
            if (
                $i == 73
                || $i == 83
                || $i == 90
            ) {
                $infoP += $v;
            }
            if (
                $i == 74
                || $i == 75
                || $i == 76
                || $i == 77
                || $i == 78
                || $i == 79
                || $i == 80
                || $i == 81
                || $i == 82
                || $i == 84
                || $i == 85
                || $i == 86
                || $i == 87
                || $i == 88
                || $i == 89
                || $i == 91
                || $i == 92
                || $i == 93
            ) {
                $infoM += $v;
            }
            $i++;
        }

        //総合
        $sum = array_sum($score);

        foreach ($score as $k => $v) {
            //読み取り力
            if ($k >= 1 && $k <= 21) {
                $read += $v;
            }
            if ($k >= 73 && $k <= 93) {
                $read += $v;
            }

            //切り替え
            if ($k >= 22 && $k <= 41) {
                $change += $v;
            }
            if ($k >= 94 && $k <= 109) {
                $change += $v;
            }
            //理解
            if ($k >= 42 && $k <= 59) {
                $understand += $v;
            }
            if ($k >= 110 && $k <= 123) {
                $understand += $v;
            }
            //選択力
            if ($k >= 60 && $k <= 72) {
                $select += $v;
            }
            if ($k >= 124 && $k <= 143) {
                $select += $v;
            }
        }

        $jyohos   = ($infoP / 3) - ($infoM / 18);

        //偏差値
        $sougo   = (($sum - $array_vf['sougo']['ave']) * 10) / $array_vf['sougo']['hen'] + 50;
        $reads   = (($read - $array_vf['read']['ave']) * 10) / $array_vf['read']['hen'] + 50;
        $changes = (($change - $array_vf['change']['ave']) * 10) / $array_vf['change']['hen'] + 50;
        $unders  = (($understand - $array_vf['understand']['ave']) * 10) / $array_vf['understand']['hen'] + 50;
        $selects = (($select - $array_vf['select']['ave']) * 10) / $array_vf['select']['hen'] + 50;

        $jyoho   = (($jyohos - $array_vf['vaias']['ave']) * 10) / $array_vf['vaias']['hen'] + 50;

        //偏差値が20以下の場合は20になる
        //偏差値が80以上の場合は80になる
        $sougo   = round($sougo, 2);
        $reads   = round($reads, 2);
        $changes = round($changes, 2);
        $unders  = round($unders, 2);
        $selects = round($selects, 2);
        $jyoho   = round($jyoho, 2);

        if ($sougo <= 20) {
            $sougo = 20.0;
        } elseif ($sougo >= 80) {
            $sougo = 80.0;
        }
        if ($reads <= 20) {
            $reads = 20.0;
        } elseif ($reads >= 80) {
            $reads = 80.0;
        }
        if ($changes <= 20) {
            $changes = 20.0;
        } elseif ($changes >= 80) {
            $changes = 80.0;
        }

        if ($unders <= 20) {
            $unders = 20.0;
        } elseif ($unders >= 80) {
            $unders = 80.0;
        }

        if ($selects <= 20) {
            $selects = 20.0;
        } elseif ($selects >= 80) {
            $selects = 80.0;
        }

        if ($jyoho <= 20) {
            $jyoho = 20.0;
        } elseif ($jyoho >= 80) {
            $jyoho = 80.0;
        }


        //スコアを得点に変更

        $lists = array();
        $lists = array(
                    "sougo" => $sougo
                    ,"yomitori" => $reads
                    ,"rikai" => $unders
                    ,"sentaku" => $selects
                    ,"kirikae" => $changes
                    ,"jyoho" => $jyoho
                );
        return $lists;

    }

